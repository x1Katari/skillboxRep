public class Line {
    private String number;
    private String name;

    public Line() {
        this.name = name;
        this.number = number;
    }
    public String getNumber() {
        return number;
    }
    public String getName() {
        return name;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public void setName(String name) {
        this.name = name;
    }
}
